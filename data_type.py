# Strings 

print('Hello World!')
print("Hello World!")
print(type("Hello World")) # The result will be str


# Number

print(type(30)) # The result will be int
print(type(30.5)) # The result will be float

# Boolean

print(type(True)) # The result will be bool
print(type(False)) # The result will be bool

# List 

print(type([10, 20, 30]))
print(type(['Hello', 'Hi', 'Aloha']))
print(type(['hello', 10, 10.5, True]))

# Tuples

print(type((10, 20, 30)))

